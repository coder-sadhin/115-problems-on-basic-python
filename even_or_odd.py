# problem: 2. Even or Odd: Write a Python program that takes an integer as input and prints whether it is even or odd

number=int(input('Enter your number.   '))

if (number % 2) == 0 :
    print(f"Your numer {number} is Even.")
else:
    print(f"Your numer {number} is Odd.")

